def get_class_name_by_index(index):
    """
    인덱스를 통해 음식 클래스 이름을 반환합니다.
    
    Args:
        index (int): 클래스 인덱스
        
    Returns:
        str: 음식 클래스 이름
    """
    if 0 <= index < len(MOBILENET_CLASSES):
        return MOBILENET_CLASSES[index]
    else:
        return None

