    """
    음식 클래스 이름을 통해 인덱스를 반환합니다.
    
    Args:
        class_name (str): 음식 클래스 이름
        
    Returns:
        int: 클래스 인덱스, 없으면 -1
    """
    try:
        return MOBILENET_CLASSES.index(class_name)
    except ValueError:
        return -1